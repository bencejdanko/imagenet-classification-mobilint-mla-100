class ModelReporter:
    def __init__(
        self,
        cfg: ReportConfig,
        gradcam_fn: Optional[Callable[[torch.nn.Module, torch.Tensor, int], np.ndarray]] = None,
    ) -> None:
        self.cfg = cfg
        self.gradcam_fn = gradcam_fn  # can be None to skip cams

        self.root = Path(cfg.out_dir) / cfg.run_name
        _ensure_dir(self.root)

    @torch.no_grad()
    def evaluate(
        self,
        model: torch.nn.Module,
        val_loader: torch.utils.data.DataLoader,
        device: torch.device,
        criterion: Optional[torch.nn.Module] = None,
    ) -> Dict[str, Any]:
        model.eval()

        all_true: List[int] = []
        all_pred: List[int] = []
        all_logits: List[np.ndarray] = []
        all_files: List[str] = []
        all_imgs: List[torch.Tensor] = []  # store CPU CHW tensors for selected examples later

        total_loss = 0.0
        total = 0

        for b, batch in enumerate(val_loader):
            if self.cfg.max_batches is not None and b >= self.cfg.max_batches:
                break

            # Support either (images, labels) or (images, labels, filenames)
            if len(batch) == 2:
                images, labels = batch
                filenames = [f"sample_{total+i:06d}" for i in range(images.size(0))]
            else:
                images, labels, filenames = batch
                filenames = list(filenames)

            images = images.to(device)
            labels = labels.to(device)

            logits = model(images)

            if criterion is not None:
                loss = criterion(logits, labels).item()
                total_loss += loss

            pred = logits.argmax(dim=1)

            bs = labels.size(0)
            total += bs

            all_true.extend(labels.detach().cpu().tolist())
            all_pred.extend(pred.detach().cpu().tolist())
            all_logits.append(_to_numpy(logits))

            # keep filenames + raw images on CPU for later rendering of examples
            all_files.extend([str(x) for x in filenames])
            all_imgs.extend([img.detach().cpu() for img in images.detach().cpu()])

        logits_np = np.concatenate(all_logits, axis=0) if all_logits else np.zeros((0, self.cfg.num_classes), dtype=np.float32)
        probs_np = _softmax_np(logits_np) if logits_np.shape[0] > 0 else logits_np

        cm = confusion_matrix(all_true, all_pred, labels=list(range(self.cfg.num_classes)))
        report_dict = classification_report(
            all_true,
            all_pred,
            labels=list(range(self.cfg.num_classes)),
            output_dict=True,
            zero_division=0,
        )

        # Extract per-class F1
        per_class_f1 = np.zeros((self.cfg.num_classes,), dtype=np.float32)
        for i in range(self.cfg.num_classes):
            per_class_f1[i] = float(report_dict.get(str(i), {}).get("f1-score", 0.0))

        acc = float((np.array(all_true) == np.array(all_pred)).mean()) if total > 0 else 0.0
        avg_loss = float(total_loss / max(1, (b + 1))) if criterion is not None else float("nan")

        return {
            "loss": avg_loss,
            "acc": acc,
            "true": np.array(all_true, dtype=np.int64),
            "pred": np.array(all_pred, dtype=np.int64),
            "logits": logits_np,
            "probs": probs_np,
            "filenames": all_files,
            "images_cpu": all_imgs,  # list of CHW tensors
            "confusion_matrix": cm,
            "classification_report": report_dict,
            "per_class_f1": per_class_f1,
        }

    def _select_top_mistakes(self, eval_out: Dict[str, Any]) -> List[int]:
        true = eval_out["true"]
        pred = eval_out["pred"]
        probs = eval_out["probs"]

        wrong = np.where(true != pred)[0]
        if wrong.size == 0:
            return []

        # "High-value mistakes" = high confidence but wrong
        conf = probs[wrong, pred[wrong]]
        order = np.argsort(-conf)  # descending
        top = wrong[order][: self.cfg.top_k_examples]
        return top.tolist()

    def build_epoch_report(
        self,
        model: torch.nn.Module,
        val_loader: torch.utils.data.DataLoader,
        device: torch.device,
        epoch: int,
        criterion: Optional[torch.nn.Module] = None,
        extra_meta: Optional[Dict[str, Any]] = None,
    ) -> Path:
        """
        Generates images + JSON + optional PDF for a given epoch.
        Returns the path to the PDF if generated, otherwise returns the epoch dir path.
        """
        cfg = self.cfg
        epoch_dir = self.root / f"epoch_{epoch:03d}"
        ex_dir = epoch_dir / "examples"
        _ensure_dir(epoch_dir)
        _ensure_dir(ex_dir)

        eval_out = self.evaluate(model, val_loader, device=device, criterion=criterion)

        # --- Plots
        cm_path = epoch_dir / "confusion_matrix.png"
        worst_path = epoch_dir / "worst_classes.png"
        _plot_confusion_matrix(eval_out["confusion_matrix"], cfg, cm_path)
        _plot_worst_classes(eval_out["per_class_f1"], cfg, worst_path, k=min(8, cfg.num_classes))

        # --- Example rendering (image + gradcam overlay)
        top_idx = self._select_top_mistakes(eval_out)
        examples: List[Dict[str, Any]] = []
        for rank, i in enumerate(top_idx):
            img = eval_out["images_cpu"][i]
            t = int(eval_out["true"][i])
            p = int(eval_out["pred"][i])
            fname = eval_out["filenames"][i]
            conf = float(eval_out["probs"][i, p])

            img_path = ex_dir / f"{rank:02d}_img.png"
            cam_path = ex_dir / f"{rank:02d}_cam.png"

            _save_image_png(img, img_path, cfg.dpi)

            cam_saved = False
            if self.gradcam_fn is not None:
                # Grad-CAM requires gradients -> run with grad enabled
                cam = self.gradcam_fn(model, img, p)
                _save_cam_overlay_png(img, cam, cam_path, cfg.dpi)
                cam_saved = True

            examples.append({
                "rank": rank,
                "index": int(i),
                "file": fname,
                "true": t,
                "pred": p,
                "true_name": _safe_class_name(t, cfg),
                "pred_name": _safe_class_name(p, cfg),
                "conf": conf,
                "img_path": str(img_path),
                "cam_path": str(cam_path) if cam_saved else None,
            })

        # --- JSON artifact (easy to diff / compare runs)
        meta = {
            "epoch": int(epoch),
            "num_classes": int(cfg.num_classes),
            "class_names": cfg.class_names,
            "loss": float(eval_out["loss"]) if not math.isnan(eval_out["loss"]) else None,
            "acc": float(eval_out["acc"]),
        }
        if extra_meta:
            meta.update(extra_meta)

        report_json = {
            "meta": meta,
            "plots": {
                "confusion_matrix": str(cm_path),
                "worst_classes": str(worst_path),
            },
            "examples": examples,
            "classification_report": eval_out["classification_report"],
        }

        json_path = epoch_dir / "report_data.json"
        with open(json_path, "w") as f:
            json.dump(report_json, f, indent=2)

        # --- Optional PDF
        if cfg.make_pdf_each_epoch:
            pdf_path = epoch_dir / f"report_epoch_{epoch:03d}.pdf"
            self._build_pdf(report_json, pdf_path)
            return pdf_path

        return epoch_dir

    def _build_pdf(self, report_json: Dict[str, Any], pdf_path: Path) -> None:
        styles = getSampleStyleSheet()
        doc = SimpleDocTemplate(str(pdf_path), pagesize=letter)
        story: List[Any] = []

        meta = report_json["meta"]
        plots = report_json["plots"]
        examples = report_json.get("examples", [])

        # Title + summary
        story.append(Paragraph("Model Evaluation Report", styles["Title"]))
        story.append(Spacer(1, 0.15 * inch))

        acc = meta.get("acc", 0.0)
        loss = meta.get("loss", None)
        line = f"Epoch: {meta.get('epoch')} &nbsp;&nbsp; Acc: {acc*100:.2f}%"
        if loss is not None:
            line += f" &nbsp;&nbsp; Loss: {loss:.4f}"
        story.append(Paragraph(line, styles["Normal"]))
        story.append(Spacer(1, 0.15 * inch))

        # Add plots
        for key in ["confusion_matrix", "worst_classes"]:
            p = plots.get(key)
            if p and os.path.exists(p):
                story.append(Paragraph(key.replace("_", " ").title(), styles["Heading2"]))
                story.append(Spacer(1, 0.08 * inch))
                story.append(RLImage(p, width=6.6 * inch, height=4.8 * inch))
                story.append(Spacer(1, 0.2 * inch))

        story.append(PageBreak())

        # Classification report table (compact: per-class F1 + support)
        story.append(Paragraph("Per-Class Summary", styles["Heading1"]))
        story.append(Spacer(1, 0.12 * inch))

        cr = report_json.get("classification_report", {})
        rows = [["Class", "Precision", "Recall", "F1", "Support"]]
        num_classes = int(meta.get("num_classes", 0))
        for i in range(num_classes):
            c = cr.get(str(i), {})
            rows.append([
                _safe_class_name(i, self.cfg),
                f"{c.get('precision', 0.0):.3f}",
                f"{c.get('recall', 0.0):.3f}",
                f"{c.get('f1-score', 0.0):.3f}",
                str(int(c.get('support', 0))),
            ])

        tbl = Table(rows, hAlign="LEFT", colWidths=[1.2*inch, 1.0*inch, 1.0*inch, 0.8*inch, 0.8*inch])
        tbl.setStyle(TableStyle([
            ("BACKGROUND", (0,0), (-1,0), colors.lightgrey),
            ("GRID", (0,0), (-1,-1), 0.25, colors.grey),
            ("FONTNAME", (0,0), (-1,0), "Helvetica-Bold"),
            ("FONTSIZE", (0,0), (-1,-1), 8),
            ("ALIGN", (1,1), (-1,-1), "RIGHT"),
        ]))
        story.append(tbl)
        story.append(PageBreak())

        # Example panels
        story.append(Paragraph("High-Confidence Mistakes (Examples)", styles["Heading1"]))
        story.append(Spacer(1, 0.12 * inch))

        for ex in examples:
            title = (
                f"{ex.get('file','')}"
                f" — True: {ex.get('true_name')}  |  Pred: {ex.get('pred_name')}"
                f"  (conf {ex.get('conf',0.0):.2f})"
            )
            story.append(Paragraph(title, styles["Normal"]))
            story.append(Spacer(1, 0.06 * inch))

            imgp = ex.get("img_path")
            camp = ex.get("cam_path")

            # Side-by-side images if cam exists; otherwise just raw image
            if imgp and os.path.exists(imgp) and camp and os.path.exists(camp):
                row = [[RLImage(imgp, width=3.2*inch, height=3.2*inch),
                        RLImage(camp, width=3.2*inch, height=3.2*inch)]]
                story.append(Table(row, colWidths=[3.3*inch, 3.3*inch]))
            elif imgp and os.path.exists(imgp):
                story.append(RLImage(imgp, width=4.2*inch, height=4.2*inch))

            story.append(Spacer(1, 0.18 * inch))

        doc.build(story)

