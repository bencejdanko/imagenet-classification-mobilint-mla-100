def _ensure_dir(p: Path) -> None:
    p.mkdir(parents=True, exist_ok=True)

def _to_numpy(x: torch.Tensor) -> np.ndarray:
    return x.detach().cpu().numpy()

def _safe_class_name(i: int, cfg: ReportConfig) -> str:
    if cfg.class_names and 0 <= i < len(cfg.class_names):
        return cfg.class_names[i]
    return str(i)

def _denorm_to_01(img: torch.Tensor) -> torch.Tensor:
    # If user already provides [0,1], do nothing.
    # If not, clamp to [0,1] to avoid ugly plots.
    return img.clamp(0, 1)

def _save_image_png(img_chw_01: torch.Tensor, out_path: Path, dpi: int) -> None:
    img = _denorm_to_01(img_chw_01).permute(1, 2, 0).detach().cpu().numpy()
    plt.figure()
    plt.imshow(img)
    plt.axis("off")
    plt.tight_layout(pad=0)
    plt.savefig(out_path, dpi=dpi, bbox_inches="tight", pad_inches=0)
    plt.close()

def _save_cam_overlay_png(img_chw_01: torch.Tensor, cam_hw_01: np.ndarray, out_path: Path, dpi: int) -> None:
    img = _denorm_to_01(img_chw_01).permute(1, 2, 0).detach().cpu().numpy()
    cam = np.clip(cam_hw_01, 0.0, 1.0)

    plt.figure()
    plt.imshow(img)
    plt.imshow(cam, alpha=0.45)  # overlay heatmap
    plt.axis("off")
    plt.tight_layout(pad=0)
    plt.savefig(out_path, dpi=dpi, bbox_inches="tight", pad_inches=0)
    plt.close()

def _plot_confusion_matrix(cm: np.ndarray, cfg: ReportConfig, out_path: Path) -> None:
    plt.figure(figsize=(8, 7))
    plt.imshow(cm)
    plt.title("Confusion Matrix")
    plt.xlabel("Predicted")
    plt.ylabel("True")
    plt.colorbar()
    plt.tight_layout()
    plt.savefig(out_path, dpi=cfg.dpi)
    plt.close()

def _plot_worst_classes(per_class_f1: np.ndarray, cfg: ReportConfig, out_path: Path, k: int = 8) -> None:
    k = min(k, cfg.num_classes)
    idx = np.argsort(per_class_f1)[:k]
    labels = [_safe_class_name(int(i), cfg) for i in idx]
    vals = per_class_f1[idx]

    plt.figure(figsize=(9, 4))
    plt.bar(range(k), vals)
    plt.xticks(range(k), labels, rotation=30, ha="right")
    plt.ylim(0, 1)
    plt.title(f"Worst {k} Classes by F1")
    plt.ylabel("F1")
    plt.tight_layout()
    plt.savefig(out_path, dpi=cfg.dpi)
    plt.close()

def _softmax_np(logits: np.ndarray) -> np.ndarray:
    logits = logits - logits.max(axis=1, keepdims=True)
    exp = np.exp(logits)
    return exp / (exp.sum(axis=1, keepdims=True) + 1e-12)


