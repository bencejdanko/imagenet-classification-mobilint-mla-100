def show_samples_by_class(ds, title, class_names=None, samples_per_class=3, num_classes=20, seed=0):
    """
    Shows `samples_per_class` samples for each class in order: 0..num_classes-1.
    Assumes ds[i] returns (img_tensor, label). If ds has `img_labels`, prints filename too.
    """
    rng = np.random.default_rng(seed)

    # Collect indices per label
    idxs_by_label = defaultdict(list)
    for i in range(len(ds)):
        item = ds[i]
        img, lab = item[0], item[1]
        idxs_by_label[int(lab)].append(i)

    # Plot grid: one row per class, N columns = samples_per_class
    rows = num_classes
    cols = samples_per_class
    plt.figure(figsize=(4 * cols, 2.8 * rows))
    plt.suptitle(title, y=0.995)

    plot_i = 1
    for lab in range(num_classes):
        idxs = idxs_by_label.get(lab, [])
        if len(idxs) == 0:
            # Empty row placeholder
            for c in range(cols):
                ax = plt.subplot(rows, cols, plot_i)
                ax.axis("off")
                if c == 0:
                    name = class_names[lab] if class_names and lab < len(class_names) else str(lab)
                    ax.set_title(f"class {lab} ({name})\nNO SAMPLES", fontsize=9)
                plot_i += 1
            continue

        # Choose samples_per_class indices (random if more available, else repeat fewer)
        if len(idxs) >= cols:
            chosen = rng.choice(idxs, size=cols, replace=False).tolist()
        else:
            chosen = idxs + rng.choice(idxs, size=(cols - len(idxs)), replace=True).tolist()

        for c, idx in enumerate(chosen):
            img, y = ds[idx]
            y = int(y)

            fname = None
            if hasattr(ds, "img_labels"):
                fname = ds.img_labels[idx][0]

            name = class_names[y] if class_names and y < len(class_names) else str(y)

            ax = plt.subplot(rows, cols, plot_i)
            ax.imshow(img.permute(1, 2, 0).clamp(0, 1))
            ax.axis("off")

            # Put label text once per row (first column), keep others clean
            if c == 0:
                ax.set_title(f"class {y} ({name})\n{fname}", fontsize=9)
            else:
                ax.set_title(f"{fname}", fontsize=8)

            plot_i += 1

    plt.tight_layout(rect=[0, 0, 1, 0.992])
    plt.show()


