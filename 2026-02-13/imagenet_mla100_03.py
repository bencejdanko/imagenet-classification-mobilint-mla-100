# Default Grad-CAM for your SimpleDNN
# Matches your current model structure:
#   features -> classifier(conv1x1) -> global_pool -> logits
# (mirrors get_gradcam(...) in your script) :contentReference[oaicite:3]{index=3}
# ----------------------------
def default_gradcam_simplednn(model: torch.nn.Module, image_chw: torch.Tensor, target_class: int) -> np.ndarray:
    model.eval()

    if image_chw.dim() == 3:
        image = image_chw.unsqueeze(0)
    else:
        image = image_chw

    image = image.to(next(model.parameters()).device)

    # Forward through feature extractor
    features = model.features(image)
    features.requires_grad_(True)
    features.retain_grad()

    # Manual forward head
    x = model.classifier(features)
    x = model.global_pool(x)
    logits = x.squeeze(3).squeeze(2)

    model.zero_grad(set_to_none=True)
    logits[0, int(target_class)].backward()

    grads = features.grad.mean(dim=[2, 3], keepdim=True)
    cam = (grads * features).sum(dim=1).squeeze()
    cam = F.relu(cam)

    cam = cam.detach().cpu().numpy()
    cam = cam - cam.min()
    cam = cam / (cam.max() + 1e-7)
    return cam


